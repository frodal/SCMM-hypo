##----------------------------------------------------------------------
## Import libraries
##----------------------------------------------------------------------
import glob
from pathlib import Path
import shutil
##----------------------------------------------------------------------
## Main
##----------------------------------------------------------------------
def main():
    abaqusPath = Path(__file__).parent.joinpath('Abaqus')
    for folder in glob.glob(str(abaqusPath)+'/*/'):
        print('Deleting directory: '+folder)
        shutil.rmtree(folder)
##----------------------------------------------------------------------
## Entry point
##----------------------------------------------------------------------
if __name__=='__main__':
    main()